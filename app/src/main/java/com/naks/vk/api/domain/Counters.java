package com.naks.vk.api.domain;

public class Counters {

    private int albums;
    private int videos;
    private int audios;
    private int photos;
    private int notes;
    private int friends;
    private int groups;
    private int onlineFriends;
    private int mutualFriends;
    private int userVideos;
    private int followers;
    private int pages;

    public int getAlbums() {
        return albums;
    }
    public void setAlbums(int albums) {
        this.albums = albums;
    }

    public int getVideos() {
        return videos;
    }
    public void setVideos(int videos) {
        this.videos = videos;
    }

    public int getAudios() {
        return audios;
    }
    public void setAudios(int audios) {
        this.audios = audios;
    }

    public int getPhotos() {
        return photos;
    }
    public void setPhotos(int photos) {
        this.photos = photos;
    }

    public int getNotes() {
        return notes;
    }
    public void setNotes(int notes) {
        this.notes = notes;
    }

    public int getFriends() {
        return friends;
    }
    public void setFriends(int friends) {
        this.friends = friends;
    }

    public int getGroups() {
        return groups;
    }
    public void setGroups(int groups) {
        this.groups = groups;
    }

    public int getOnlineFriends() {
        return onlineFriends;
    }
    public void setOnlineFriends(int onlineFriends) {
        this.onlineFriends = onlineFriends;
    }

    public int getMutualFriends() {
        return mutualFriends;
    }
    public void setMutualFriends(int mutualFriends) {
        this.mutualFriends = mutualFriends;
    }

    public int getUserVideos() {
        return userVideos;
    }
    public void setUserVideos(int userVideos) {
        this.userVideos = userVideos;
    }

    public int getFollowers() {
        return followers;
    }
    public void setFollowers(int followers) {
        this.followers = followers;
    }

    public int getPages() {
        return pages;
    }
    public void setPages(int pages) {
        this.pages = pages;
    }
}
